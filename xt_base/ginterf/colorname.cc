// Static function.
// Look up color by name and return true with RGB if found.
//
bool
GRcolorList::lookupColor(const char *name, int *red, int *green, int *blue)
{
    if (!name)
        return (false);
    while (isspace(*name))
        name++;
    if (!*name)
        return (false);
    if (*name == '#') {
        // accept "#rrggbb"
        unsigned cx;
        if (sscanf(name+1, "%x", &cx) != 1)
            return (false);
        *blue = cx & 0xff;
        *green = (cx & 0xff00) >> 8;
        *red = (cx & 0xff0000) >> 16;
        return (true);
    }

    char cbuf[64];
    strncpy(cbuf, name, 64);
    cbuf[63] = 0;
    char *s = cbuf + strlen(cbuf) - 1;
    while (isspace(*s))
        *s-- = 0;

    for (int i = 0; color_list[i].name; i++) {
        if (lstring::cieq(cbuf, color_list[i].name)) {
            *red = color_list[i].red;
            *green = color_list[i].green;
            *blue = color_list[i].blue;
            return (true);
        }
    }

    // try again, elide spaces from name
    s = cbuf;
    char *t = s;
    bool spc = false;
    while (*t) {
        if (*t == ' ')
            spc = true;
        else
            *s++ = *t;
        t++;
    }
    *s = 0;

    if (spc) {
        for (int i = 0; color_list[i].name; i++) {
            if (lstring::cieq(cbuf, color_list[i].name)) {
                *red = color_list[i].red;
                *green = color_list[i].green;
                *blue = color_list[i].blue;
                return (true);
            }
        }
    }
    return (false);
}


// Static function.
// Return a list of the colors and RGB values.
//
stringlist *
GRcolorList::listColors()
{
    char tbuf[128];
    stringlist *s0 = 0, *se = 0;
    for (int i = 0; color_list[i].name; i++) {
        snprintf(tbuf, sizeof(tbuf), "%3d %3d %3d  %s",
            color_list[i].red, color_list[i].green, color_list[i].blue,
            color_list[i].name);
        if (!s0)
            s0 = se = new stringlist(lstring::copy(tbuf), 0);
        else {
            se->next = new stringlist(lstring::copy(tbuf), 0);
            se = se->next;
        }
    }
    return (s0);
}

